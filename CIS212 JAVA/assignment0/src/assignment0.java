
import java.io.File;
import java.io.FileNotFoundException;
import java.util.Scanner;
public class assignment0 {  
	public static void main(String[] args) throws FileNotFoundException{
	Scanner x;
	x = new Scanner(new File("inSample.txt"));
	x.nextLine();
	while(x.hasNext()){     
		int a = x.nextInt();  
		int b = x.nextInt();    
		System.out.println(addMultNum(a,b));
		}  
	x.close();
	}
	public static String addMultNum(int a, int b){
		return(a+b + " " + a*b);
	}

}


 